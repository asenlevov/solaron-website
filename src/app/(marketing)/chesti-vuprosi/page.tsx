const allItems: FaqItem[] = sections.flatMap((s) => s.items);

export default function ChestiVuprosiPage() {
  const [query, setQuery] = useState("");

  const filteredSections = useMemo(() => {
    const q = query.trim().toLowerCase();
    if (!q) return sections;
    return sections
      .map((s) => ({
        ...s,
        items: s.items.filter(
          (it) =>
            it.q.toLowerCase().includes(q) ||
            it.a.toLowerCase().includes(q),
        ),
      }))
      .filter((s) => s.items.length > 0);
  }, [query]);

  const faqJsonLd = {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    mainEntity: allItems.map((it) => ({
      "@type": "Question",
      name: it.q,
      acceptedAnswer: {
        "@type": "Answer",
        text: it.a,
      },
    })),
  };

  return (
    <main className="pt-16">
      <SectionWrapper background="gray">
        <SectionHeader
          label="ПОМОЩ"
          title="Често Задавани Въпроси"
          subtitle="Отговори по теми от общи въпроси до монтаж, ценообразуване, техника и административни стъпки."
        />

        <div className="relative mx-auto mb-12 max-w-xl">
          <Search
            className="pointer-events-none absolute left-4 top-1/2 size-5 -translate-y-1/2 text-foreground-tertiary"
            aria-hidden
          />
          <label htmlFor="faq-search" className="sr-only">
            Търсене във въпросите
          </label>
          <input
            id="faq-search"
            type="search"
            value={query}
            onChange={(e) => setQuery(e.target.value)}
            placeholder="Търсене по ключова дума…"
            className="w-full rounded-xl border border-border-medium bg-background py-3 pl-12 pr-4 font-body text-foreground shadow-sm outline-none transition-[border-color,box-shadow] placeholder:text-foreground-tertiary focus:border-accent focus:ring-2 focus:ring-accent/20"
          />
        </div>

        <div className="space-y-14">
          {filteredSections.map((section) => (
            <section key={section.key} aria-labelledby={`faq-${section.key}-h`}>
              <h2
                id={`faq-${section.key}-h`}
                className="border-b border-border pb-3 font-display text-2xl font-semibold text-foreground"
              >
                {section.title}
              </h2>
              <Accordion.Root type="multiple" className="mt-4 space-y-2">
                {section.items.map((item) => (
                  <Accordion.Item
                    key={item.id}
                    value={item.id}
                    className="overflow-hidden rounded-xl border border-border bg-background-card shadow-card"
                  >
                    <Accordion.Header>
                      <Accordion.Trigger
                        className={cn(
                          "flex w-full items-start justify-between gap-4 px-5 py-4 text-left font-body text-sm font-semibold text-foreground transition-colors hover:bg-background-secondary md:text-base",
                          "data-[state=open]:bg-background-secondary",
                        )}
                      >
                        <span>{item.q}</span>
                        <ChevronDown
                          className="mt-0.5 size-5 shrink-0 text-accent transition-transform duration-200 data-[state=open]:rotate-180"
                          aria-hidden
                        />
                      </Accordion.Trigger>
                    </Accordion.Header>
                    <Accordion.Content className="overflow-hidden">
                      <div className="border-t border-border px-5 pb-5 pt-0">
                        <p className="pt-4 font-body text-sm leading-relaxed text-foreground-secondary md:text-base">
                          {item.a}
                        </p>
                      </div>
                    </Accordion.Content>
                  </Accordion.Item>
                ))}
              </Accordion.Root>
            </section>
          ))}
        </div>

        {filteredSections.length === 0 ? (
          <p className="mt-8 text-center font-body text-foreground-secondary">
            Няма съвпадения. Променете търсенето или изчистете полето.
          </p>
        ) : null}
      </SectionWrapper>

      <SectionWrapper background="white">
        <div className="flex flex-col items-center text-center py-12">
          <p className="text-editorial-overline text-accent mb-3">Не намирате отговор?</p>
          <h2 className="font-display text-2xl font-bold md:text-3xl mb-4">
            Свържете се с нас
          </h2>
          <p className="max-w-md text-foreground-secondary mb-8">
            Нашият екип е на разположение за безплатна консултация и отговори на всички ваши въпроси.
          </p>
          <div className="flex flex-col gap-3 sm:flex-row">
            <MagneticButton href="/kontakti" variant="primary" size="lg">
              Безплатна Консултация
            </MagneticButton>
            <MagneticButton href="/konfigurator" variant="outline" size="lg">
              Конфигуратор
            </MagneticButton>
          </div>
        </div>
      </SectionWrapper>

      <script
        type="application/ld+json"
        dangerouslySetInnerHTML={{ __html: JSON.stringify(faqJsonLd) }}
      />
    </main>
  );
}
