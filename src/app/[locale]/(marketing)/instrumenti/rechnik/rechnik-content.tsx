export function RechnikContent() {
  const [query, setQuery] = useState("");

  const filtered = useMemo(() => {
    const q = query.trim().toLowerCase();
    if (!q) return terms;
    return terms.filter(
      (t) =>
        t.term.toLowerCase().includes(q) ||
        t.definition.toLowerCase().includes(q),
    );
  }, [query]);

  const schema = {
    "@context": "https://schema.org",
    "@type": "DefinedTermSet",
    name: "Соларен речник | Solaron",
    description:
      "Основни понятия от фотоволтаиката, мрежата и енергийната регулация в България.",
    hasDefinedTerm: terms.map((t) => ({
      "@type": "DefinedTerm",
      name: t.term,
      description: t.definition,
    })),
  };

  return (
    <main className="pt-16">
      <SectionWrapper background="gray">
        <SectionHeader
          label="ИНСТРУМЕНТИ"
          title="Соларен Речник"
          subtitle="Кратки дефиниции на често срещани термини при проектиране, монтаж и администриране на фотоволтаици."
        />

        <div className="mx-auto mb-10 max-w-xl">
          <label htmlFor="rechnik-search" className="sr-only">
            Търсене в речника
          </label>
          <input
            id="rechnik-search"
            type="search"
            value={query}
            onChange={(e) => setQuery(e.target.value)}
            placeholder="Търсене по термин или описание…"
            className="w-full rounded-xl border border-border-medium bg-background px-4 py-3 font-body text-foreground shadow-sm outline-none transition-[border-color,box-shadow] placeholder:text-foreground-tertiary focus:border-accent focus:ring-2 focus:ring-accent/20"
          />
        </div>

        <section aria-label="Списък с термини">
          <dl className="space-y-6">
            {filtered.map((item) => (
              <div
                key={item.term}
                className="rounded-xl border border-border bg-background-card p-5 shadow-card md:p-6"
              >
                <dt
                  className={cn(
                    "font-display text-lg font-semibold text-accent md:text-xl",
                  )}
                >
                  {item.term}
                </dt>
                <dd className="mt-2 font-body text-sm leading-relaxed text-foreground-secondary md:text-base">
                  {item.definition}
                </dd>
              </div>
            ))}
          </dl>
        </section>

        {filtered.length === 0 ? (
          <p className="mt-8 text-center font-body text-foreground-secondary">
            Няма резултати за това търсене.
          </p>
        ) : null}
      </SectionWrapper>

      <script
        type="application/ld+json"
        dangerouslySetInnerHTML={{ __html: JSON.stringify(schema) }}
      />
    </main>
  );
}
